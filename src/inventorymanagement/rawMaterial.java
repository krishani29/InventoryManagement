    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // update record

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectRow = jTable1.getSelectedRow();

        int sno = Integer.parseInt((String) model.getValueAt(selectRow, 0));
        String mid = matid.getText();
        String mtype = mattype.getSelectedItem().toString();
        String dname = delar_name.getText();
        String contact = delar_contact.getText();
        String qty = mqty.getText();
        String date=sdf.format(jDateChooser1.getDate());
        String disc = jTextArea2.getText();
        String price = mprice.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            int row = jTable1.getSelectedRow();
            String value = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "update raw_material set mid='" + mid + "',materialtype='" + mtype + "',delarname='" + dname + "',contact='" + contact + "',qty='" + qty + "',mdate='" + date + "',disc='" + disc + "',price='" + price + "' where sno='" + value + "' ";  //"++" use this pattern to pass variables
            int n = st.executeUpdate(sql);         //use this for insert/update/delte query and for searching ExecuteQuery
            if (n == 1) {
                JOptionPane.showMessageDialog(this, n + " records modified successfully..");
            } else {
                JOptionPane.showMessageDialog(this, "No ID Found..!");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

        loadData();
        clear();
    }//GEN-LAST:event_jButton4ActionPerformed

