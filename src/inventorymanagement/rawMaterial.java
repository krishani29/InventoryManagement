    public void loadData() {
        try {
            String sql = "select * from raw_material";
            rs = st.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("sno"), rs.getString("mid"), rs.getString("materialtype"), rs.getString("delarname"), rs.getString("contact"), rs.getString("qty"), rs.getString("mdate"), rs.getString("price"), rs.getString("disc")});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        total();

    }

    public void total() {
        int numrow = jTable1.getRowCount();

        double total = 0;
        for (int i = 0; i < numrow; i++) {
            double val = Double.valueOf(jTable1.getValueAt(i, 7).toString());
            total += val;
        }
        this.total.setText(Double.toString(total) + " Rs.");

    }

