    public void total() {
        int numrow = jTable1.getRowCount();

        double total = 0;
        for (int i = 0; i < numrow; i++) {
            double val = Double.valueOf(jTable1.getValueAt(i, 8).toString());
            total += val;
        }
        this.total.setText(Double.toString(total) + " Rs");

    }
