    public void loadData() {
        try {
            String sql = "select * from sale";
            rs = st.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString("id"), rs.getString("rate"), rs.getString("customer_id"), rs.getString("customer_name"), rs.getString("contact"), rs.getString("sale_date"), rs.getString("order_type"), rs.getString("dty"), rs.getString("amount"), rs.getString("disc"),rs.getString("m_id")});
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
        total();

    }

